# a) listado ordenado por nombre y por especie;
def nombre_especie (lista):
    lista.sort(key=by_name)
    lista.sort(key=by_species)
    return lista

# b) mostrar toda la información de Ahsoka Tano y Kit Fisto;
def ahsoka_kit (lista):
    ahsoka=search(lista, 'name', "Ahsoka Tano")
    kit=search(lista, 'name', "Kit Fisto")
    print(f"la informacion de Ahsoka Tano es {lista[ahsoka]}")
    print(f"la informacion de Kit Fisto es {lista[kit]}")

def dividir(jedi, criterio):
    maestro=jedi[criterio]
    if maestro != None:
        if "/" in maestro:
            maestros=maestro.split("/")
            return maestros
        else:
            return maestro
    else: 
        return None
    
# c) mostrar todos los padawan de Yoda y Luke Skywalker, es decir sus aprendices;
def padawan (lista):
    for jedi in lista:
        maestro=dividir(jedi, 'master')
        if type(maestro) == list:
            for master in maestro:
                if master == "Yoda" or master == "Luke Skywalker":
                    print(f"{jedi['name']} es padawan de {master}")
        else:
            if maestro == "Yoda" or maestro == "Luke Skywalker":
                print(f"{jedi['name']} es padawan de {maestro}")

# d) mostrar los Jedi de especie humana y twi'lek;
def especie(lista):
    for jedi in lista:
        especie=dividir(jedi, 'species')
        if type(especie) == list:
            for species in especie:
                if species== "Human" or species == "Twi'lek":
                    print(f"El jedi {jedi['name']} es de especie {species}")
        else:
            if especie== "Human" or especie == "Twi'lek":
                    print(f"El jedi {jedi['name']} es de especie {especie}")

# e) listar todos los Jedi que comienzan con A;
def jedi_A (lista):
    lista_A = []
    for jedi in lista:
        nombres=dividir(jedi, 'name')
        if type(nombres) == list:
            for nombre in nombres:
                if nombre[0] == "A":
                    lista_A.append(jedi)
        else:
            if nombres[0] == "A":
                lista_A.append(jedi)
    return lista_A

